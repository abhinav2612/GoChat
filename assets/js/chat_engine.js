class ChatEngine{
    constructor(chatBoxId, userEmail){
        this.chatBox = $(`#${chatBoxId}`);
        this.userEmail = userEmail;

        this.socket = io.connect('http://localhost:5000');

        if (this.userEmail){
            this.connectionHandler();
        }

    }


    connectionHandler(){
        let self = this;

        this.socket.on('connect', function(){
            console.log('connection established using sockets...!');


            self.socket.emit('join_room', {
                user_email: self.userEmail,
                chatroom: 'GoChat'
            });

            self.socket.on('user_joined', function(data){
                console.log('a user joined!', data);
            })


        });

        // CHANGE :: send a message on clicking the send message button
        $('#name-btn').click(function(){          
            URL = `http://localhost:5000`;
            window.location = URL;
        });
        if(window.location.href.includes(`http://localhost:8000`)){
            $('#user-chat-box').hide();
        }
        if(window.location.href.includes(`http://localhost:5000`)){
            $('#start-chat').hide();
            $('#user-chat-box').show();
            $('#heading').text('GoChat Chat Room');
            // if click on sign-out button redirect back to localhost:3000/users/sign-in
            $('#sign-out').click(function(){
                URL = `http://localhost:8000/users/sign-in`;
                window.location = URL;
            });
        }
        $('#send-message').click(function(){
            let msg = $('#chat-message-input').val();

            if (msg != ''){
                self.socket.emit('send_message', {
                    message: msg,
                    user_email: self.userEmail,
                    chatroom: 'GoChat'
                });
            }
        });

        self.socket.on('receive_message', function(data){
            console.log('message received', data.message);


            let newMessage = $('<li>');

            let messageType = 'other-message';

            if (data.user_email == self.userEmail){
                messageType = 'self-message';
            }

            newMessage.append($('<span>', {
                'html': data.message
            }));

            newMessage.append($('<sub>', {
                'html': data.user_email
            }));

            newMessage.addClass(messageType);

            $('#chat-messages-list').append(newMessage);
        })
    }
}